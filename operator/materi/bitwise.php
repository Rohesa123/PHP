<?php

$a = 60;
$b = 13;

// bitwise AND
$c = $a & $b;
echo "$a & $b = $c";
echo "<br>";

// bitwise OR
$c = $a | $b;
echo "$a | $b = $c";
echo "<br>";

// bitwise XOR
$c = $a ^ $b;
echo "$a ^ $b = $c";
echo "<br>";

// bitwise Left
$c = $a << $b;
echo "$a << $b = $c";
echo "<br>";

// bitwise Right
$c = $a >> $b;
echo "$a >> $b = $c";
echo "<br>";
